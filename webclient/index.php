            <div class="explain">
                <img src="pict/img/iconfinder_map-map-marker_299035.png">
                <br><p>Minimarket merupakan sebuah toko kecil yang menjual segala kebutuhan sehari-hari, seperti bahan makanan, minuman, makanan ringan, minuman kaleng, aksesoris, produk kosmetik, peralatan rumah tangga, dan kebutuhan keseharian lainnya.</p>
                
                <p>Seiring dengan berjalannya waktu semakin banyak pula kebutuhan setiap orang, sehingga manusia tidak bisa terlepas dari yang namanya pasar untuk membeli setiap kebutuhan sehari-hari. Bahkan minimarket adalah tempat yang dikunjungi setiap hari oleh sebagian orang. Tetapi sebelum memasuki minimarket, pembeli belum mengetahui apa saja yang ada di dalam minimarket tersebut, dan calon pembeli tidak mengetahui dimana saj terdapat minimarket di lingkungan sekitarnya. Maka dibutuhkan sebuah sistem yang mampu menampilkan letak minimarket dan atribut apa saja yang ada di dalamnya.</p>
